#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*out;
	size_t	j;	
	size_t	i;

	i = 0;
	j = 0;
	while (s[i] != '\0' && s[i] != c)
		i++;
	if (s[i] == c)
	{
		while (s[i])
		{
			out[j] = s[i];
			j++;
			i++;
		}
	}
	out[j] = '\0';
	return (out);
}
